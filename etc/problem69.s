PROGRAM PROBLEM69

INCLUDE LIBRARY

BYTE PRIMES[1000,1000]
WORD I
WORD J
REAL VALUE
REAL INDEX

BEGIN

FILL PRIMES, 1000000., TRUE
PRIMES[0,0]=TRUE

FOR I= 0 TO 999
  FOR J= 0 TO 999
    IF PRIMES[I, J]
      VALUE=I:. * 1000. + J:.
      INDEX=VALUE + VALUE
      WHILE INDEX < 1000000.
        PRIMES[(INDEX / 1000.):+, INDEX:+ % 1000]=FALSE
        INDEX=INDEX + VALUE

OUTPUT "#I#C", PRIMES[0, 2]:-
OUTPUT "#I#C", PRIMES[0, 3]:-
OUTPUT "#I#C", PRIMES[0, 4]:-
OUTPUT "#I#C", PRIMES[0, 5]:-

END
