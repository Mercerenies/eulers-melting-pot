data ZModule = ZModule Integer Integer Integer Integer
               deriving stock (Eq, Generic)
               deriving anyclass Hashable

cTerm :: ZModule -> Integer
cTerm (ZModule c _ _ _) = c

c2Term :: ZModule -> Integer
c2Term (ZModule _ c2 _ _) = c2

sTerm :: ZModule -> Integer
sTerm (ZModule _ _ s _) = s

scTerm :: ZModule -> Integer
scTerm (ZModule _ _ _ sc) = sc

instance Show ZModule where
    showsPrec _ (ZModule 0 0 0 0) = ("0" ++)
    showsPrec _ (ZModule c c2 s sc) = foldr (.) id . intersperse (" + " ++) $ mapMaybe term [(c, "c"), (c2, "c^2"), (s, "s"), (sc, "s c")]
        where term (0, _) = Nothing
              term (n, s) = Just (shows n . showString " " . showString s)

-- Half-baked impl mainly to get fromInteger (but also (+) is nice)
instance Num ZModule where
    ZModule c c2 s sc + ZModule c' c2' s' sc' = ZModule (c + c') (c2 + c2') (s + s') (sc + sc')
    negate (ZModule c c2 s sc) = ZModule (-c) (-c2) (-s) (-sc)
    (*) = error "Unimplemented (*)"
    abs = error "Unimplemented abs"
    signum = error "Unimplemented signum"
    fromInteger n = n .* one

one :: ZModule
one = ZModule (-2) 4 0 0

(.*) :: Integer -> ZModule -> ZModule
n .* (ZModule c c2 s sc) = ZModule (c * n) (c2 * n) (s * n) (sc * n)

data RobotPosition = RobotPosition {
      robotDir :: Int, -- 0 to 4 inclusive.
      robotX :: ZModule,
      robotY :: ZModule
    }
    deriving stock (Show, Eq, Generic)
    deriving anyclass (Hashable)

instance Semigroup RobotPosition where
    RobotPosition d x y <> RobotPosition d' x' y' = RobotPosition ((d + d') `mod` 5) (x + x') (y + y')

instance Monoid RobotPosition where
    mempty = startPosition

-- NOTE: I start the robot facing east just because it made the trig
-- simpler when I was drawing the diagram. No other differences, since
-- it's just a 90 degree rotation of the whole problem. We are working
-- in mathematical coordinates, so positive Y is up and
-- "counterclockwise" means "positive angle".
startPosition :: RobotPosition
startPosition = RobotPosition 0 0 0

data Move = Clockwise | Anticlockwise
            deriving (Show, Read, Eq, Ord, Enum, Bounded)

