moveToAction :: RobotPosition -> Move -> RobotPosition
moveToAction (robotDir -> dir) move = RobotPosition dir' (cos36n moveDir) (sin36n moveDir)
    where dir' = case move of
                   Clockwise -> 4
                   Anticlockwise -> 1
          moveDir = case move of
                      Clockwise -> (2 * dir - 1) `mod` 10
                      Anticlockwise -> (2 * dir + 1) `mod` 10

applyMove :: Move -> RobotPosition -> RobotPosition
applyMove move robot = robot <> moveToAction robot move

applyMoves :: RobotPosition -> [Move] -> RobotPosition
applyMoves = foldl' (flip applyMove)

applyMovesToStart :: [Move] -> RobotPosition
applyMovesToStart = applyMoves startPosition

