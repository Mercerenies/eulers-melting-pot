-- Analysis of Problem 208. (all angles are degrees)
--
-- Write c = cos(36) and s = sin(36). For now, treat these strictly as
-- symbolic variables (we will simplify them later).
--
-- The only directions the robot can face are multiples of 72. And
-- some straightforward trigonometry tells us that if the robot is
-- facing at angle α, then the directions the robot can move are α ±
-- 36. So all angles relevant to this problem are multiples of 36.
--
-- The robot's state is a 3-tuple (d, x, y). d is the direction (in
-- multiples of 72, so there are five options), and x and y are the
-- coordinates.
--
-- Assume the robot starts at (0, 0, 0). Then a "move" involves moving
-- the robot in a legal direction, which adds cos(36N) to x and
-- sin(36N) to y (where N is determined by the direction of motion).
--
-- Through some elbow grease and Paul Dawkins' wonderful trig
-- reference
-- (https://tutorial.math.lamar.edu/pdf/Trig_Cheat_Sheet_Reduced.pdf),
-- we enumerate the possible values of these expressions below.
--
-- cos(0) = 1
-- cos(36) = c
-- cos(72) = 2 c^2 - 1
-- cos(108) = 1 - 2 c^2
-- cos(144) = - c
-- cos(180) = - 1
-- cos(216) = - c
-- cos(252) = 1 - 2 c^2
-- cos(288) = 2 c^2 - 1
-- cos(324) = c
--
-- sin(0) = 0
-- sin(36) = s
-- sin(72) = 2 s c
-- sin(108) = 2 s c
-- sin(144) = s
-- sin(180) = 0
-- sin(216) = - s
-- sin(252) = - 2 s c
-- sin(288) = - 2 s c
-- sin(324) = - s
--
-- Observations: Every one of these is a polynomial in 1, c, c^2, s,
-- and s c. And we only ever add linear multiples of these, so we're
-- working in some module over Z. Now let's get some real values for
-- these numbers. WolframAlpha tells us that
--
-- c = cos(36) = 1/4 + sqrt(5)/4
--
-- s = sin(36) = 1/2 * sqrt(1/2 * (5 - sqrt(5)))
--
-- Let's start with the s. I never square an s, so it's only used
-- linearly. No other term here has a square root with anything
-- resembling that radical (to be precise, that radical is likely an
-- extension of degree 4 over Q, where the only other radical is
-- sqrt(5) which clearly has degree 2). So in a polynomial with these
-- terms, all of the s and s c^2 terms must be zero in order for the
-- polynomial to be zero.
--
-- c is a bit more complicated. Forgetting about s for a minute, we're
-- looking at the space generated by {1, c, c^2}, which is three
-- elements and we know that a basis of this space is {1, sqrt(5)} (so
-- the module has length 2). So there must be linear dependence in {1,
-- c, c^2}. A bit of linear algebra gives us that this linear
-- independence can be expressed as 0 = 1 + 2 c - 4 c^2. Module theory
-- tells us that (up to linear multiples) this is the only nontrivial
-- formula in these three variables. Hence, 1 = 4 c^2 - 2 c.
-- Substituting this into the above formulas gives us a new table.
--
-- cos(0) = 4 c^2 - 2 c
-- cos(36) = c
-- cos(72) = 2 c - 2 c^2
-- cos(108) = 2 c^2 - 2 c
-- cos(144) = - c
-- cos(180) = - 4 c^2 + 2 c
-- cos(216) = - c
-- cos(252) = 2 c^2 - 2 c
-- cos(288) = 2 c - 2 c^2
-- cos(324) = c
--
-- sin(0) = 0
-- sin(36) = s
-- sin(72) = 2 s c
-- sin(108) = 2 s c
-- sin(144) = s
-- sin(180) = 0
-- sin(216) = - s
-- sin(252) = - 2 s c
-- sin(288) = - 2 s c
-- sin(324) = - s
--
-- This fully represents a free Z-module of length 4 with basis {c,
-- c^2, s, s c}.
--
-- Runs in about 13 seconds. GHC optimizations do not appear to help
-- or hinder.

